def populate():
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()

    cursor.execute("""
        CREATE TABLE IF NOT EXISTS world_lore (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            category TEXT,
            topic TEXT,
            content TEXT
        )
    """)

    cursor.execute("DELETE FROM world_lore")

    for category, topic, content in lore_entries:
        cursor.execute(
            "INSERT INTO world_lore (category, topic, content) VALUES (?, ?, ?)",
            (category, topic, content),
        )

    conn.commit()
    conn.close()
    print(f"[完成] 已寫入 {len(lore_entries)} 條世界觀資料到 world_lore 表格。")


if __name__ == "__main__":
    populate()
